    }
}

// Test configuration
function test_configuration()
{
    global $f2b, $config, $db_config;
    $results = [];

    // Test fail2ban connection
    require_once('engine.inc.php');
    $socket_test = check_socket();
    $results['fail2ban'] = [
        'name' => 'Fail2Ban Connection',
        'status' => ($socket_test === 'OK'),
        'message' => $socket_test
    ];

    // Test database connection (if enabled)
    if (isset($config['use_central_db']) && $config['use_central_db'] === true) {
        require_once('db.inc.php');
        try {
            $db = get_db_connection();
            $results['database'] = [
                'name' => 'Database Connection',
                'status' => ($db !== null),
                'message' => $db ? 'Connected successfully' : 'Connection failed'
            ];
        } catch (Exception $e) {
            $results['database'] = [
                'name' => 'Database Connection',
                'status' => false,
                'message' => $e->getMessage()
            ];
        }
    }

    // Test DNS resolution (if enabled)
    if (isset($f2b['usedns']) && $f2b['usedns'] === true) {
        $start = microtime(true);
        $hostname = @gethostbyaddr('8.8.8.8');
        $duration = round((microtime(true) - $start) * 1000, 2);

        $results['dns'] = [
            'name' => 'DNS Resolution',
            'status' => true,
            'message' => "Resolved in {$duration}ms (Result: {$hostname})"
        ];
    }

    // Test cache
    require_once('cache.inc.php');
    $cache_test_key = 'admin_test_' . time();
    cache_set($cache_test_key, 'test_value', 10);
    $cache_result = cache_get($cache_test_key);

    $results['cache'] = [
        'name' => 'Cache System',
        'status' => ($cache_result === 'test_value'),
        'message' => function_exists('apcu_fetch') ? 'Using APCu' : 'Using file cache'
    ];

    return $results;
}

// Helper function to get value from JSON config
function get_json_value($path, $default = '')
{
    global $json_config;
    $keys = explode('.', $path);
    $value = $json_config;

    foreach ($keys as $key) {
        if (!isset($value[$key])) {
            return $default;
        }
        $value = $value[$key];
    }

    return $value;
}
