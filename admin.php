// Check authentication and session timeout
require_authentication(1800, 'index.php');

$success_message = '';
$error_message = '';
$test_results = [];

// Load JSON configuration
$json_config_path = __DIR__ . '/config.json';
$json_config = [];

if (file_exists($json_config_path)) {
    $json_content = file_get_contents($json_config_path);
    $json_config = json_decode($json_content, true);
    if (json_last_error() !== JSON_ERROR_NONE) {
        $error_message = 'Failed to parse config.json: ' . json_last_error_msg();
        $json_config = [];
    }
}

// Handle save configuration
if ($_SERVER['REQUEST_METHOD'] === 'POST' && isset($_POST['save_config'])) {
    if (!csrf_verify()) {
        $error_message = 'CSRF validation failed';
    } else {
        // Create backup
        if (file_exists($json_config_path)) {
            $backup_path = $json_config_path . '.backup.' . date('YmdHis');
            if (copy($json_config_path, $backup_path)) {
                $success_message = "Backup created: " . basename($backup_path) . "<br>";
            } else {
                $error_message = "Failed to create backup file";
            }
        }

        if (!$error_message) {
            // Build JSON configuration
            $new_config = [
                '_comment' => 'Fail2Ban Web Interface Configuration - Generated by admin.php on ' . date('Y-m-d H:i:s'),
                'environment' => $_POST['environment'] ?? 'production',
                'title' => $_POST['title'] ?? 'Fail2Ban Dashboard',
                'server' => [
                    'name' => $_POST['server_name'] ?? 'my-server',
                    'ip' => $_POST['server_ip'] ?? '127.0.0.1'
                ],
                'database' => [
                    'enabled' => isset($_POST['use_central_db']),
                    'host' => $_POST['db_host'] ?? 'localhost',
                    'port' => (int)($_POST['db_port'] ?? 3306),
                    'name' => $_POST['db_database'] ?? 'fail2ban_central',
                    'username' => $_POST['db_username'] ?? 'fail2ban_user',
                    'password' => $_POST['db_password'] ?? '',
                    'charset' => 'utf8mb4'
                ],
                'authentication' => [
                    'users' => []
                ],
                'fail2ban' => [
                    'socket' => $_POST['f2b_socket'] ?? '/var/run/fail2ban/fail2ban.sock',
                    'use_socket_check' => isset($_POST['f2b_use_socket_check']),
                    'usedns' => isset($_POST['f2b_usedns']),
                    'dns_async' => isset($_POST['f2b_dns_async']),
                    'dns_timeout' => (int)($_POST['f2b_dns_timeout'] ?? 2),
                    'dns_cache_ttl' => (int)($_POST['f2b_dns_cache_ttl'] ?? 86400),
                    'noempt' => isset($_POST['f2b_noempt']),
                    'jainfo' => isset($_POST['f2b_jainfo'])
                ],
                'version' => '0.4 (2025)'
            ];

            // Handle authentication user
            $auth_user = [
                'username' => $_POST['auth_user'] ?? 'admin'
            ];

            // If new password provided, hash it
            if (!empty($_POST['auth_password_new'])) {
                $auth_user['password_hash'] = password_hash($_POST['auth_password_new'], PASSWORD_DEFAULT);
                $auth_user['_comment'] = 'Password updated on ' . date('Y-m-d H:i:s');
            } else {
                // Keep existing password_hash if available
                if (isset($json_config['authentication']['users'][0]['password_hash'])) {
                    $auth_user['password_hash'] = $json_config['authentication']['users'][0]['password_hash'];
                } elseif (!empty($_POST['auth_password'])) {
                    // Fallback to plain password if it exists
                    $auth_user['password'] = $_POST['auth_password'];
                }
            }

            $new_config['authentication']['users'][] = $auth_user;

            // Write JSON file
            $json_output = json_encode($new_config, JSON_PRETTY_PRINT | JSON_UNESCAPED_SLASHES);

            if ($json_output === false) {
                $error_message = 'Failed to encode JSON: ' . json_last_error_msg();
            } else {
                if (file_put_contents($json_config_path, $json_output) !== false) {
                    $success_message .= 'Configuration saved successfully to config.json';
                    // Reload configuration
                    $json_config = $new_config;
                } else {
                    $error_message = 'Failed to write config.json. Check file permissions.';
                }
            }
        }
    }
}

// Handle test configuration
